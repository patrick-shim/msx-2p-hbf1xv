"""Deterministic 720 KB 2DD disk-image generator (M16-S6 A/B parity fixture).

Produces `tests/parity/m16_boot.dsk`: a byte-for-byte port of
`src/devices/fdc/disk_image.cpp` `DiskImage::synthesize()` (720 KB / 737,280
bytes, 80 cylinders x 2 sides x 9 sectors x 512 bytes, media descriptor 0xF9),
so the SAME committed, deterministic medium can be presented to BOTH this
emulator (which already mounts exactly this synthesized image by default at
every `cold_boot()`) and to openMSX (`-diska tests/parity/m16_boot.dsk`) for
the planner-mandated identical-medium A/B comparison
(docs/m16-planner-package.md Section 5.1 / Section 7).

This is a PURE FUNCTION of constants only -- no host-filesystem or wall-clock
input feeds the generated bytes (planner Section 5.1 / A-M16-4). Running this
script twice produces a byte-identical file; its SHA256 is reproducible and
auditable via tools/gates/checksum-assets.ps1-style verification.

Usage:
  python tools/gen/boot-disk.py [-o tests/parity/m16_boot.dsk]
  python tools/gen/boot-disk.py --self-check
"""
import argparse
import hashlib
import sys

SECTOR_SIZE = 512
SECTORS_PER_TRACK = 9
SIDES = 2
TRACKS = 80
TOTAL_SECTORS = SECTORS_PER_TRACK * SIDES * TRACKS  # 1440
IMAGE_BYTES = TOTAL_SECTORS * SECTOR_SIZE            # 737280
MEDIA_DESCRIPTOR = 0xF9


def synthesize() -> bytearray:
    """Port of DiskImage::synthesize() (src/devices/fdc/disk_image.cpp)."""
    image = bytearray(IMAGE_BYTES)

    # Per-sector deterministic fill: a pure function of (lba, offset).
    for lba in range(TOTAL_SECTORS):
        base = lba * SECTOR_SIZE
        for j in range(SECTOR_SIZE):
            image[base + j] = (lba * 13 + j * 7 + 0x5A) & 0xFF

    # Standard MSX 720 KB FAT12 boot sector + BPB at LBA 0.
    image[0] = 0xEB  # JMP short
    image[1] = 0xFE
    image[2] = 0x90  # NOP
    oem = b"SONYMSX "
    image[3:11] = oem
    # BPB (offset 11):
    image[11] = 0x00  # bytes/sector = 512
    image[12] = 0x02
    image[13] = 0x02  # sectors/cluster = 2
    image[14] = 0x01  # reserved sectors = 1
    image[15] = 0x00
    image[16] = 0x02  # number of FATs = 2
    image[17] = 0x70  # root dir entries = 112
    image[18] = 0x00
    image[19] = 0xA0  # total sectors = 1440
    image[20] = 0x05
    image[21] = MEDIA_DESCRIPTOR
    image[22] = 0x03  # sectors/FAT = 3
    image[23] = 0x00
    image[24] = 0x09  # sectors/track = 9
    image[25] = 0x00
    image[26] = 0x02  # heads = 2
    image[27] = 0x00
    image[28] = 0x00  # hidden sectors = 0
    image[29] = 0x00
    # Boot signature.
    image[510] = 0x55
    image[511] = 0xAA

    # FAT copies at LBA 1 and LBA 4 (1 reserved + 3 sectors/FAT).
    for fat_lba in (1, 4):
        fat_base = fat_lba * SECTOR_SIZE
        image[fat_base + 0] = MEDIA_DESCRIPTOR
        image[fat_base + 1] = 0xFF
        image[fat_base + 2] = 0xFF
        for j in range(3, SECTOR_SIZE):
            image[fat_base + j] = 0x00

    return image


def main(argv):
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("-o", "--output", default="tests/parity/m16_boot.dsk",
                        help="output path (default tests/parity/m16_boot.dsk)")
    parser.add_argument("--self-check", action="store_true",
                        help="verify determinism (two syntheses byte-identical) and exit")
    args = parser.parse_args(argv[1:])

    image_a = synthesize()
    if args.self_check:
        image_b = synthesize()
        ok = (bytes(image_a) == bytes(image_b) and len(image_a) == IMAGE_BYTES and
              image_a[510] == 0x55 and image_a[511] == 0xAA and image_a[21] == MEDIA_DESCRIPTOR)
        print("self-check:", "PASS" if ok else "FAIL")
        return 0 if ok else 1

    with open(args.output, "wb") as f:
        f.write(image_a)
    digest = hashlib.sha256(image_a).hexdigest()
    print("wrote {} ({} bytes), sha256={}".format(args.output, len(image_a), digest))
    return 0


if __name__ == "__main__":
    sys.exit(main(sys.argv))
