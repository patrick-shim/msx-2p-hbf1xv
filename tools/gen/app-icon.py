#!/usr/bin/env python3
"""Embed the owner-provided MSX taskbar icon into the SDL window-icon header.

The app/taskbar artwork is OWNER-PROVIDED final art under assets/icons/ (the
real MSX logo) -- this project no longer draws its own icon. The only generated
artifact is the C header holding the runtime window/taskbar icon pixels for
SDL_SetWindowIcon(): we load the owner's taskbar PNG, downscale to 64x64 RGBA
with high-quality LANCZOS, and emit it in the header format the frontend expects.

  source : assets/icons/msx_taskbar_1024.png   (owner-provided taskbar variant)
  output : src/frontend/app_icon_data.h         (64x64 RGBA, kAppIconRGBA[])

The .exe/Explorer icon is handled separately by the linked Windows resource
(src/frontend/app_icon.rc.in -> assets/icons/msx_app.ico); this tool does not
touch that path.

Usage:  python tools/gen/app-icon.py
"""

import os
from PIL import Image

TASKBAR_PNG = os.path.join("assets", "icons", "msx_taskbar_1024.png")
HEADER = os.path.join("src", "frontend", "app_icon_data.h")
SIZE = 64


def main():
    here = os.path.dirname(os.path.abspath(__file__))
    root = os.path.dirname(here)

    src = os.path.join(root, TASKBAR_PNG)
    img = Image.open(src).convert("RGBA").resize((SIZE, SIZE), Image.LANCZOS)
    raw = img.tobytes()  # RGBA, row-major, SIZE*SIZE*4 bytes

    lines = [
        "// Auto-generated by tools/gen/app-icon.py -- do not edit by hand.",
        "// %dx%d RGBA (row-major, non-premultiplied) window/taskbar icon for" % (SIZE, SIZE),
        "// SDL_SetWindowIcon(), downscaled from the owner-provided",
        "// assets/icons/msx_taskbar_1024.png. Regenerate: python tools/gen/app-icon.py",
        "#ifndef SONY_MSX_FRONTEND_APP_ICON_DATA_H",
        "#define SONY_MSX_FRONTEND_APP_ICON_DATA_H",
        "",
        "namespace sony_msx::frontend {",
        "",
        "inline constexpr int kAppIconWidth = %d;" % SIZE,
        "inline constexpr int kAppIconHeight = %d;" % SIZE,
        "",
        "// RGBA8888, %d bytes." % len(raw),
        "inline constexpr unsigned char kAppIconRGBA[] = {",
    ]
    for i in range(0, len(raw), 16):
        lines.append("    " + ",".join("0x%02x" % b for b in raw[i:i + 16]) + ",")
    lines += [
        "};",
        "",
        "}  // namespace sony_msx::frontend",
        "",
        "#endif  // SONY_MSX_FRONTEND_APP_ICON_DATA_H",
    ]

    out = os.path.join(root, HEADER)
    with open(out, "w", newline="\n", encoding="utf-8") as f:
        f.write("\n".join(lines))
    print("wrote:", out, "(from %s, %d bytes RGBA)" % (TASKBAR_PNG, len(raw)))


if __name__ == "__main__":
    main()
